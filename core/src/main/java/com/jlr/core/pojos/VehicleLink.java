package com.jlr.core.pojos;

/**
 * The type Primary link.
 *
 * @author Adobe
 */
public class VehicleLink {

    /** The text. */
    private String text;
    
    /** The accessible text. */
    private String accessibleText;
    
    /** The internal link. */
    private String internalLink;
    
    /** The external link. */
    private String externalLink;
    
    /** The target keyword. */
    private String targetKeyword;
    
    /** The icon. */
    private String icon;
    
    /** The href. */
    private String href;

    /**
     * Gets text.
     *
     * @return the text
     */
    public String getText() {
        return text;
    }

    /**
     * Sets text.
     *
     * @param text the text
     */
    public void setText(String text) {
        this.text = text;
    }

    /**
     * Gets accessible text.
     *
     * @return the accessible text
     */
    public String getAccessibleText() {
        return accessibleText;
    }

    /**
     * Sets accessible text.
     *
     * @param accessibleText the accessible text
     */
    public void setAccessibleText(String accessibleText) {
        this.accessibleText = accessibleText;
    }

    /**
     * Gets internal link.
     *
     * @return the internal link
     */
    public String getInternalLink() {
        return internalLink;
    }

    /**
     * Sets internal link.
     *
     * @param internalLink the internal link
     */
    public void setInternalLink(String internalLink) {
        this.internalLink = internalLink;
    }

    /**
     * Gets external link.
     *
     * @return the external link
     */
    public String getExternalLink() {
        return externalLink;
    }

    /**
     * Sets external link.
     *
     * @param externalLink the external link
     */
    public void setExternalLink(String externalLink) {
        this.externalLink = externalLink;
    }

    /**
     * Gets target keyword.
     *
     * @return the target keyword
     */
    public String getTargetKeyword() {
        return targetKeyword;
    }

    /**
     * Sets target keyword.
     *
     * @param targetKeyword the target keyword
     */
    public void setTargetKeyword(String targetKeyword) {
        this.targetKeyword = targetKeyword;
    }

    /**
     * Gets icon.
     *
     * @return the icon
     */
    public String getIcon() {
        return icon;
    }

    /**
     * Sets icon.
     *
     * @param icon the icon
     */
    public void setIcon(String icon) {
        this.icon = icon;
    }

    /**
     * Gets href.
     *
     * @return the href
     */
    public String getHref() {
        return href;
    }

    /**
     * Sets href.
     *
     * @param href the href
     */
    public void setHref(String href) {
        this.href = href;
    }
}
